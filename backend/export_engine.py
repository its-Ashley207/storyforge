def export_epub(story: dict, chapters: list[dict]) -> bytes:
    """
    Render story + chapters as an EPUB 2 e-book.

    Returns raw EPUB bytes using ebooklib.
    """
    from ebooklib import epub

    book = epub.EpubBook()

    story_title = story.get("title", "Untitled Story")
    genre = story.get("genre", "")
    summary = story.get("summary", "").strip()

    book.set_identifier(f"storyforge-{story.get('id', 'unknown')}")
    book.set_title(story_title)
    book.set_language("en")
    book.add_author("The Story Forge")

    if genre:
        book.add_metadata("DC", "subject", genre)
    if summary:
        book.add_metadata("DC", "description", summary)

    # ---- CSS ----
    css_content = """
body {
    font-family: Georgia, 'Times New Roman', serif;
    font-size: 1em;
    line-height: 1.7;
    margin: 1em 1.5em;
    color: #222;
}
h1.book-title {
    font-size: 2em;
    text-align: center;
    margin-top: 2em;
    color: #1a1a2e;
}
h2.subtitle {
    font-size: 1em;
    text-align: center;
    color: #666;
    margin-top: 0.5em;
}
p.synopsis-label {
    font-variant: small-caps;
    font-weight: bold;
    margin-top: 2em;
    color: #444;
}
p.synopsis {
    font-style: italic;
    color: #333;
}
h2.chapter-title {
    font-size: 1.5em;
    margin-top: 2em;
    margin-bottom: 0.5em;
    color: #1a1a2e;
    page-break-before: always;
}
p {
    text-indent: 1.2em;
    margin: 0 0 0.4em 0;
}
p.no-indent {
    text-indent: 0;
}
p.word-count {
    font-size: 0.8em;
    color: #999;
    text-align: right;
    margin-top: 1em;
    text-indent: 0;
}
hr {
    border: none;
    border-top: 1px solid #ccc;
    margin: 1.5em auto;
    width: 60%;
}
"""
    nav_css = epub.EpubItem(
        uid="style_nav",
        file_name="style/nav.css",
        media_type="text/css",
        content=css_content,
    )
    book.add_item(nav_css)

    # ---- Title / intro page ----
    meta_parts = list(filter(None, [genre, story.get("tone", "")]))
    meta_str = " · ".join(meta_parts)

    intro_html = f"""<?xml version='1.0' encoding='utf-8'?>
<!DOCTYPE html PUBLIC '-//W3C//DTD XHTML 1.1//EN'
  'http://www.w3.org/TR/xhtml11/DTD/xhtml11.dtd'>
<html xmlns='http://www.w3.org/1999/xhtml' xml:lang='en'>
<head>
  <title>{_html_escape(story_title)}</title>
  <link rel='stylesheet' type='text/css' href='style/nav.css'/>
</head>
<body>
  <h1 class='book-title'>{_html_escape(story_title)}</h1>
  {f"<h2 class='subtitle'>{_html_escape(meta_str)}</h2>" if meta_str else ""}
  <hr/>
  {_synopsis_html(summary)}
  <p class='no-indent' style='text-align:center;margin-top:3em;color:#999;font-size:0.85em;'>
    Created with The Story Forge
  </p>
</body>
</html>"""

    intro_item = epub.EpubHtml(
        title="Title Page",
        file_name="intro.xhtml",
        lang="en",
    )
    intro_item.content = intro_html
    intro_item.add_item(nav_css)
    book.add_item(intro_item)

    # ---- Chapter pages ----
    epub_chapters = [intro_item]
    spine = ["nav", intro_item]

    for ch in chapters:
        ch_num = ch.get("chapter_num", "?")
        ch_title = ch.get("title", "").strip()
        content = ch.get("content", "").strip()
        word_count = ch.get("word_count", 0)

        if ch_title:
            heading = f"Chapter {ch_num}: {ch_title}"
        else:
            heading = f"Chapter {ch_num}"

        # Convert paragraphs to <p> tags
        paras_html = "\n".join(
            f"  <p>{_html_escape(p)}</p>"
            for p in content.split("\n")
            if p.strip()
        )

        wc_html = (
            f"<p class='word-count'>[{word_count:,} words]</p>"
            if word_count else ""
        )

        ch_html = f"""<?xml version='1.0' encoding='utf-8'?>
<!DOCTYPE html PUBLIC '-//W3C//DTD XHTML 1.1//EN'
  'http://www.w3.org/TR/xhtml11/DTD/xhtml11.dtd'>
<html xmlns='http://www.w3.org/1999/xhtml' xml:lang='en'>
<head>
  <title>{_html_escape(heading)}</title>
  <link rel='stylesheet' type='text/css' href='style/nav.css'/>
</head>
<body>
  <h2 class='chapter-title'>{_html_escape(heading)}</h2>
{paras_html}
{wc_html}
</body>
</html>"""

        ch_item = epub.EpubHtml(
            title=heading,
            file_name=f"chapter_{ch_num:03d}.xhtml",
            lang="en",
        )
        ch_item.content = ch_html
        ch_item.add_item(nav_css)
        book.add_item(ch_item)
        epub_chapters.append(ch_item)
        spine.append(ch_item)

    # ---- ToC and spine ----
    book.toc = [epub.Link(item.file_name, item.title, item.id)
                for item in epub_chapters]
    book.add_item(epub.EpubNcx())
    book.add_item(epub.EpubNav())
    book.spine = spine

    # Write to bytes buffer
    buffer = io.BytesIO()
    epub.write_epub(buffer, book, {})
    return buffer.getvalue()


def _html_escape(text: str) -> str:
    """Minimal HTML entity escaping for EPUB content."""
    return (
        str(text)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
        .replace("'", "&#39;")
    )


def _synopsis_html(summary: str) -> str:
    """Render synopsis paragraphs as HTML, or empty string if blank."""
    if not summary.strip():
        return ""
    paras = "\n".join(
        f"  <p class='synopsis'>{_html_escape(p)}</p>"
        for p in summary.split("\n")
        if p.strip()
    )
    return f"<p class='synopsis-label'>Synopsis</p>\n{paras}"
